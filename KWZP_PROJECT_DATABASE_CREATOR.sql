CREATE FUNCTION [dbo].[podziel_miesiac](@_idokresu INT)
RETURNS FLOAT AS
BEGIN
	IF (@_idokresu = 1 OR @_idokresu = 4) --miesiac lub zlecenie
		return 1; --czyli 20 dni
	ELSE	IF @_idokresu = 2 --tydzien
		return 0.25; -- czyli 5 dni
	ELSE IF @_idokresu = 3 -- dzien
		return 0.05 -- czyli 1 dzien 
	ELSE IF @_idokresu = 5 --godzina
		return 0.00625 -- czyli 1 godzina	
return NULL
END
Go

