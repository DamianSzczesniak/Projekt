--WIDOKI PRODUKCJA--

--1) WYKORZYSTANE_MASZYNY -> wypisanie wszystkich maszyn wykorzystywanych podczas wytworzenia jednego produktu
--NALEZY DOPISAC DO UTWORZENIA WIDOKU:
GO
CREATE VIEW WYKORZYSTANE_MASZYNY AS
SELECT NAZWA_PRODUKTU, mm.MODEL, mm.MARKA, mm.TYP_MASZYNY
FROM PRODUKT p
LEFT JOIN PRODUKCJA_POLPRODUKTU pp ON pp.ID_PRODUKTU = p.ID_PRODUKTU
LEFT JOIN POLPRODUKTY plp ON plp. ID_POLPRODUKTU = pp. ID_POLPRODUKTU
LEFT JOIN DOKUMENTACJA d ON d.ID_DOKUMENTACJI = plp.ID_DOKUMENTACJI
LEFT JOIN DOKUMENTACJA_PROCES dp ON dp.ID_DOKUMENTACJI = d.ID_DOKUMENTACJI
LEFT JOIN PROCES pr ON pr.ID_PROCESU = dp.ID_PROCESU
LEFT JOIN MASZYNY m ON m.ID_MASZYNY = pr.ID_MASZYNY
LEFT JOIN MODELE_MASZYN mm ON mm.ID_MODEL_MASZYNY = m.ID_MODEL_MASZYNY
GO


--2) WYKORZYSTANE_NARZEDZIA -> wypisanie wszystkich narzedzi wykorzystywanych podczas wytworzenia jednego produktu
--NALEZY DOPISAC DO UTWORZENIA WIDOKU:
CREATE VIEW WYKORZYSTANE_NARZEDZIA AS
SELECT NAZWA_PRODUKTU ,nn.NAZWA, nn.MODEL
FROM PRODUKT p
LEFT JOIN PRODUKCJA_POLPRODUKTU pp ON pp.ID_PRODUKTU = p.ID_PRODUKTU
LEFT JOIN POLPRODUKTY plp ON plp. ID_POLPRODUKTU = pp. ID_POLPRODUKTU
LEFT JOIN DOKUMENTACJA d ON d.ID_DOKUMENTACJI = plp.ID_DOKUMENTACJI
LEFT JOIN DOKUMENTACJA_PROCES dp ON dp.ID_DOKUMENTACJI = d.ID_DOKUMENTACJI
LEFT JOIN PROCES pr ON pr.ID_PROCESU = dp.ID_PROCESU
LEFT JOIN PROCES_NARZEDZIA n ON n.ID_PROCESU = pr.ID_PROCESU
LEFT JOIN NARZEDZIA nn ON nn.ID_NARZEDZIA = n.ID_NARZEDZIA
GO



--3) WYKORZYSTANE_PROCESY -> wypisanie wszystkich procesów wykorzystanych podczas wytworzenia jednego produktu
--NALEZY DOPISAC DO UTWORZENIA WIDOKU:
CREATE VIEW WYKORZYSTANE_PROCESY AS
SELECT NAZWA_PRODUKTU, slp.NAZWA_PROCESU
FROM PRODUKT p
LEFT JOIN PRODUKCJA_POLPRODUKTU pp ON pp.ID_PRODUKTU = p.ID_PRODUKTU
LEFT JOIN POLPRODUKTY plp ON plp. ID_POLPRODUKTU = pp. ID_POLPRODUKTU
LEFT JOIN DOKUMENTACJA d ON d.ID_DOKUMENTACJI = plp.ID_DOKUMENTACJI
LEFT JOIN DOKUMENTACJA_PROCES dp ON dp.ID_DOKUMENTACJI = d.ID_DOKUMENTACJI
LEFT JOIN PROCES pr ON pr.ID_PROCESU = dp.ID_PROCESU
LEFT JOIN SLOWNIK_PROCES_TECHNOLOGICZNY slp ON slp.ID_SLOWNIK_PROCES_TECHNOLOGICZNY = pr.ID_SLOWNIK_PROCES_TECHNOLOGICZNY
GO



--4) OBCIAZENIE_MASZYN -> wypisanie jakiego dnia jakie maszyny były wykkorzystane
--NALEZY DOPISAC DO UTWORZENIA WIDOKU:
CREATE VIEW OBCIAZENIE_MASZYN AS 
